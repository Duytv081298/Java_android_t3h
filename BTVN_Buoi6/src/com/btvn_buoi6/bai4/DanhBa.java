package com.btvn_buoi6.bai4;

public class DanhBa {
    private String ten;
    private int sdt;

    public DanhBa(String ten, int sdt) {
        this.ten = ten;
        this.sdt = sdt;
    }

    public String getTen() {
        return ten;
    }

    public int getSdt() {
        return sdt;
    }

    public void setSdt(int sdt) {
        this.sdt = sdt;
    }

}
